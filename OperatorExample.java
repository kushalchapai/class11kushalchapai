public class OperatorExample {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x++);//paila x ko value 10 thyo post increment garda yo statement ma 10 nai hunxa next statement ma jadha badxa
        System.out.println(++x);//pre increment garda pailain x ko value badaidinxa tyahe varw 11 ko value badauda 12 vayo
        System.out.println(x--);//post rw pre method
        System.out.println(--x);
    }
}
